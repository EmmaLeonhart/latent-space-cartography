def fig_collision_heatmap(word2vec):
    """Heatmap showing cosine similarity between all diacritical words."""
    words = [w for w in DIACRITIC_WORDS if w in word2vec]
    n = len(words)
    matrix = np.zeros((n, n))
    for i in range(n):
        for j in range(n):
            matrix[i, j] = cosine(word2vec[words[i]], word2vec[words[j]])

    fig, ax = plt.subplots(figsize=(12, 10))
    im = ax.imshow(matrix, cmap="RdYlGn_r", vmin=0.3, vmax=1.0, aspect="auto")

    ax.set_xticks(range(n))
    ax.set_yticks(range(n))
    ax.set_xticklabels(words, rotation=55, ha="right", fontsize=8)
    ax.set_yticklabels(words, fontsize=8)

    for i in range(n):
        for j in range(n):
            color = "white" if matrix[i, j] > 0.8 else "black"
            ax.text(j, i, f"{matrix[i, j]:.2f}", ha="center", va="center",
                    fontsize=6, color=color)

    cbar = fig.colorbar(im, ax=ax, shrink=0.8)
    cbar.set_label("Cosine Similarity", fontsize=11)

    ax.set_title(
        "mxbai-embed-large: Cosine Similarity Between Diacritical Words\n"
        "Unrelated words collapse to cosine ~1.0 due to [UNK] token dominance",
        fontsize=12, pad=15,
    )
    plt.tight_layout()
    path = FIG_DIR / "collision_heatmap.png"
    fig.savefig(str(path), dpi=150, bbox_inches="tight")
    plt.close(fig)
    print(f"  Saved {path}")


def fig_diacritic_vs_plain(word2vec):
    """Bar chart: diacritical word vs its plain ASCII equivalent."""
    pairs = list(zip(DIACRITIC_WORDS, PLAIN_WORDS))
    pairs = [(d, p) for d, p in pairs if d in word2vec and p in word2vec]

    labels = [f"{d}\nvs {p}" for d, p in pairs]
    sims = [cosine(word2vec[d], word2vec[p]) for d, p in pairs]

    # Also show cross-collision: diacritical word vs random other diacritical word
    cross_sims = []
    for i, (d, _) in enumerate(pairs):
        other_d = pairs[(i + 3) % len(pairs)][0]
        cross_sims.append(cosine(word2vec[d], word2vec[other_d]))

    fig, ax = plt.subplots(figsize=(14, 6))
    x = np.arange(len(labels))
    w = 0.35

    bars1 = ax.bar(x - w / 2, sims, w, label="Same word (diacritic vs plain)", color="#3498db", edgecolor="white")
    bars2 = ax.bar(x + w / 2, cross_sims, w, label="Different word (diacritic vs diacritic)", color="#e74c3c", edgecolor="white")

    ax.axhline(y=0.95, color="red", linestyle="--", alpha=0.5, label="Collision threshold (0.95)")
    ax.axhline(y=0.5, color="green", linestyle="--", alpha=0.3, label="Expected baseline")

    ax.set_ylabel("Cosine Similarity", fontsize=11)
    ax.set_title(
        "The Paradox: Unrelated Words Are MORE Similar Than the Same Word\n"
        '"Hokkaid\\u014d" is closer to "\\u00c9ire" than to "Hokkaido"',
        fontsize=12,
    )
    ax.set_xticks(x)
    ax.set_xticklabels(labels, fontsize=7, rotation=30, ha="right")
    ax.set_ylim(0, 1.1)
    ax.legend(fontsize=9)
    ax.grid(True, alpha=0.2, axis="y")

    plt.tight_layout()
    path = FIG_DIR / "diacritic_vs_plain.png"
    fig.savefig(str(path), dpi=150, bbox_inches="tight")
    plt.close(fig)
    print(f"  Saved {path}")


def fig_threshold_sweep(word2vec):
    """How many collisions at each cosine threshold."""
    # Compute all pairwise diacritical similarities
    words = [w for w in DIACRITIC_WORDS if w in word2vec]
    all_sims = []
    for i in range(len(words)):
        for j in range(i + 1, len(words)):
            all_sims.append(cosine(word2vec[words[i]], word2vec[words[j]]))

    thresholds = np.arange(0.80, 1.001, 0.01)
    counts = [sum(1 for s in all_sims if s >= t) for t in thresholds]

    fig, ax = plt.subplots(figsize=(10, 5))
    ax.fill_between(thresholds, counts, alpha=0.3, color="#e74c3c")
    ax.plot(thresholds, counts, "o-", color="#e74c3c", markersize=4)

    ax.axvline(x=0.95, color="red", linestyle="--", alpha=0.7)
    ax.annotate(
        f"{sum(1 for s in all_sims if s >= 0.95)} pairs at 0.95",
        xy=(0.95, sum(1 for s in all_sims if s >= 0.95)),
        xytext=(0.88, max(counts) * 0.8),
        arrowprops=dict(arrowstyle="->", color="red"),
        fontsize=10, color="red",
    )

    total = len(words) * (len(words) - 1) // 2
    ax.axhline(y=total, color="gray", linestyle=":", alpha=0.5)
    ax.text(0.81, total + 1, f"Total pairs: {total}", fontsize=9, color="gray")

    ax.set_xlabel("Cosine Similarity Threshold", fontsize=11)
    ax.set_ylabel("Number of Colliding Pairs", fontsize=11)
    ax.set_title(
        "Collision Count by Threshold\n"
        "Nearly all diacritical word pairs collide above 0.95",
        fontsize=12,
    )
    ax.grid(True, alpha=0.2)
    plt.tight_layout()
    path = FIG_DIR / "collision_count_over_threshold.png"
    fig.savefig(str(path), dpi=150, bbox_inches="tight")
    plt.close(fig)
    print(f"  Saved {path}")


def fig_token_analysis(word2vec):
    """Show the mechanism: [UNK] token dominates the embedding."""
    # Similarity distribution: diacritical-diacritical vs control-control vs diacritical-control
    d_words = [w for w in DIACRITIC_WORDS if w in word2vec]
    c_words = [w for w in CONTROL_WORDS if w in word2vec]

    dd_sims, cc_sims, dc_sims = [], [], []
    for i in range(len(d_words)):
        for j in range(i + 1, len(d_words)):
            dd_sims.append(cosine(word2vec[d_words[i]], word2vec[d_words[j]]))
    for i in range(len(c_words)):
        for j in range(i + 1, len(c_words)):
            cc_sims.append(cosine(word2vec[c_words[i]], word2vec[c_words[j]]))
    for d in d_words:
        for c in c_words:
            dc_sims.append(cosine(word2vec[d], word2vec[c]))

    fig, axes = plt.subplots(1, 3, figsize=(15, 5))

    for ax, sims, title, color in [
        (axes[0], dd_sims, "Diacritical vs Diacritical\n(SHOULD be low)", "#e74c3c"),
        (axes[1], cc_sims, "Control vs Control\n(Baseline)", "#3498db"),
        (axes[2], dc_sims, "Diacritical vs Control\n(Cross-group)", "#f39c12"),
    ]:
        if sims:
            ax.hist(sims, bins=30, color=color, edgecolor="white", alpha=0.8)
            ax.axvline(x=np.mean(sims), color="black", linestyle="--", linewidth=2)
            ax.text(
                0.95, 0.95, f"mean={np.mean(sims):.3f}\nstd={np.std(sims):.3f}",
                transform=ax.transAxes, ha="right", va="top", fontsize=9,
                bbox=dict(boxstyle="round", facecolor="white", alpha=0.8),
            )
        ax.set_title(title, fontsize=11)
        ax.set_xlabel("Cosine Similarity")
        ax.set_xlim(-0.1, 1.1)
        ax.grid(True, alpha=0.2)

    fig.suptitle(
        "[UNK] Token Dominance: Diacritical Words Cluster Into a Single Point\n"
        "The embedding model cannot distinguish ANY diacritical word from ANY other",
        fontsize=12, y=1.02,
    )
    plt.tight_layout()
    path = FIG_DIR / "token_analysis.png"
    fig.savefig(str(path), dpi=150, bbox_inches="tight")
    plt.close(fig)
    print(f"  Saved {path}")


